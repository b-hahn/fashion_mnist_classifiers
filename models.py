class MiniVGG(nn.Module):
    def __init__(self, conv1_size=64, conv2_size=128, classifier_size=512):
        super(MiniVGG, self).__init__()
        self.first_classifer_size = classifier_size
        self.avgpool = nn.AdaptiveAvgPool2d((7, 7))

        self.vgg1 = self.vggBlock(1, self.conv1_size)
        self.vgg2 = self.vggBlock(self.conv1_size, self.conv2_size)
        self.classifier = nn.Sequential(
            nn.Linear(self.conv2_size * 7 * 7, self.first_classifer_size),
            nn.ReLU(),
            nn.Dropout(),
            nn.Linear(self.first_classifer_size, self.first_classifer_size),
            nn.ReLU(),
            nn.Linear(self.first_classifer_size, 10)
        )

    def vggBlock(self, in_dim, out_dim):
        return nn.Sequential(
            nn.Conv2d(in_dim, out_dim, 3, padding=1), # 28
            nn.ReLU(),
            nn.BatchNorm2d(out_dim),
            nn.Conv2d(out_dim, out_dim, 3, padding=1), # 28
            nn.ReLU(),
            nn.BatchNorm2d(out_dim),
            nn.MaxPool2d(2,2)
        )

    def forward(self, x):
        x = self.vgg1(x)
        x = self.vgg2(x)
        x = F.dropout(x)
        x = self.avgpool(x)
        x = torch.flatten(x, 1)
        x = self.classifier(x)
        return x



