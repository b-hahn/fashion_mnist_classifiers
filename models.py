import torch
import torch.nn as nn
import torch.nn.functional as F

class SimpleConvNet(nn.Module):
    def __init__(self, conv1_size, conv2_size, fc_size):
        super(SimpleConvNet, self).__init__()
        self.conv2_size = conv2_size
        self.conv1 = nn.Conv2d(1, conv1_size, 3, padding=1)
        self.pool = nn.MaxPool2d(2, 2)
        self.conv2 = nn.Conv2d(conv1_size, conv2_size, 3, padding=1)
        self.dropout = nn.Dropout()
        self.fc1 = nn.Linear(conv2_size * 7 * 7, fc_size)
        self.fc2 = nn.Linear(fc_size, 10)

    def forward(self, x):
        x = self.pool(F.relu(self.conv1(x)))
        x = self.pool(F.relu(self.conv2(x)))
        x = x.view(-1, self.conv2_size * 7 * 7)
        x = F.relu(self.fc1(x))
        x = self.fc2(x)
        return x


